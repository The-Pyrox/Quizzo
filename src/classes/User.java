package classes;

public class User {
	
	public User() {
		
	}
	
	String name;
	int id,score;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setId(int ID) {
		this.id = ID;
	}
	
	public int getId() {
		return id;
	}
	
	public void setScore(int score) {
		this.score = score;
	}
	
	public int getScore() {
		return score;
	}

}
